# Dictionary:

# Um dicionário é uma maneira de se associar pares de itens, para acessá-los futuramente. Utilizando Key-Value (chave-valor)

d = {1:'João', 2:'Gavassa', 3:'Henrique', 4:'Jailson'}

# Printando dicionário
print (d)

#Acessando valor utilizando as chaves.
print("Primeiro nome é: "+d[1])
print("Segundo nome é: "+d[4])

print(d.keys())
print(d.values())
