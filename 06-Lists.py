# Lists

# Podemos listar itens, utilizando algo semelhante a Arrays, porém poderá conter dados de diferentes tipos:
# Utilizamos colchetes "[]" para definir as lists.


lista1  = [1, "hi", "Python", 2]    
#Checando o tipo da lista
print(type(lista1))

#Printando a lista1
print (lista1)

# Cortando a lista (Slicing)
print (lista1[3:])

# Cortando a lista (Slicing)
print (lista1[1:3])

# Concatenando a lista utilizando o operador + 
print (lista1 + lista1)

# Repetindo a Lista utilizando o operador * 

print (lista1 * 3)