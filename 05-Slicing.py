# Slicing

# Você consegue selecionar o que deseja que apareça no print, de acordo com a variável

str1 = 'Olá Gavassa' #string str1    
str2 = ' Como está você?' #string str2   

print (str1[0:2]) #Printa os dois primeiros caracteres, usando slice operator.    
print (str1[4]) #Printa o quarto caractere da String.
print (str1*2) #Printando a String duas vezes (repetindo-a).
print (str1 + str2) #Printa as Strings concatenando elas.