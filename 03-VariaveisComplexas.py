# Variáveis Complexas

# Você consegue utilizar uma função para ver se a variável é complexa, para utilizar futuramente no programa:

# Adicione as variáveis:
a = 5  
print("The type of a", type(a))  
  
b = 40.5  
print("The type of b", type(b))  
  
c = 1+3j  
print("The type of c", type(c))  

# Teste para ver se é complexo:
print(" c is a complex number", isinstance(1+3j,complex))  
