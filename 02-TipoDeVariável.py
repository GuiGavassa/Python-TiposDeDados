# Tipo de Variável.

# Para ver qual é o tipo de variável

# Declare-as
a = 10
b = "Olá, Python"
c = 10.5

print(type(a))
print(type(b))
print(type(c))

# O output de cada uma será o tipo das mesmas.


# <class 'int'>
# <class 'str'>
# <class 'float'>


# O tipo da variável é declarado por si só, apenas dependendo da maneira em que for escrito.

# Você consegue mudar o tipo da variável dependendo do modo em que for inserido.

# Caso for adicionado um número inteiro, em uma variável que anteriormente era String, ela se tornará Int:

b = 10
print(type(b))
